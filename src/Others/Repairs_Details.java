    private void search_items_for_this_RepairActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_search_items_for_this_RepairActionPerformed
        try {
            DefaultTableModel df = (DefaultTableModel) jTable2.getModel();
            int rowCount = df.getRowCount();
            for (int i = 0; i < rowCount; i++) {
                df.removeRow(0);
            }
            ResultSet rs = MyConn.search("select * from repairs_need_items where repair_id = '" + repair_ID.getText() + "' ");
            while (rs.next()) {
                Vector v = new Vector();
                ResultSet getItem = MyConn.search("select item_name from items where item_code = '" + rs.getString(2) + "' ");
                while (getItem.next()) {
                    v.add(rs.getString(2) + " - " + getItem.getString(1));
                }
                v.add(rs.getString(3));
                df.addRow(v);
            }
        } catch (Exception e) {
            e.printStackTrace();
        }
    }//GEN-LAST:event_search_items_for_this_RepairActionPerformed

    private void search_RepairsActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_search_RepairsActionPerformed
        try {
            DefaultTableModel df = (DefaultTableModel) jTable1.getModel();
            int rowCount = df.getRowCount();
            for (int i = 0; i < rowCount; i++) {
                df.removeRow(0);
            }
            ResultSet rs = MyConn.search("select * from repairs_details");
            while (rs.next()) {
                Vector v = new Vector();
                v.add(rs.getString(1));
                v.add(rs.getString(2));
                v.add(rs.getString(3));
                v.add(rs.getString(4));
                v.add(rs.getString(5));
                if (rs.getBoolean(6)) {
                    v.add("Active");
                } else {
                    v.add("Inactive");
                }
                df.addRow(v);
            }
        } catch (Exception e) {
        }
    }//GEN-LAST:event_search_RepairsActionPerformed


